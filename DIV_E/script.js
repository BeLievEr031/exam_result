function rearrangeName(fullName) {
  // Split the full name into parts
  const parts = fullName.split(' ');


  // Rearrange the parts
  if (parts.length > 2) {
    fullName = `${parts[1]} ${parts[2]} ${parts[0]}`;
  }

  return fullName;
}

function gradeDistribution(grade) {
  if (grade === "A") {
    grade = "A1"
  } else {
    grade = "A2"
  }
  return grade;
}

function convertDate(dateString) {
  // Create a Date object from the input string
  const date = new Date(dateString);

  // Get the day, month (0-indexed), and year components
  const day = date.getDate().toString().padStart(2, '0');
  const month = (date.getMonth() + 1).toString().padStart(2, '0'); // Months are 0-indexed
  const year = date.getFullYear();

  // Format the date in the desired DD/MM/YYYY format
  const formattedDate = `${day}-${month}-${year}`;

  return formattedDate;
}

function excelSerialDateToJSDate(serialDate) {
  // Excel's epoch starts on January 1, 1900.
  const excelEpoch = new Date(1900, 0, 1);

  // Adjust for Excel's leap year bug. Excel falsely treats 1900 as a leap year.
  const leapYearAdjustment = (serialDate > 59) ? -1 : 0;

  // Calculate the correct date by adding the serial date to January 1, 1900
  // and adjusting for the leap year bug.
  const jsDate = new Date(excelEpoch.getTime() + (serialDate + leapYearAdjustment) * 86400000);

  return jsDate;
}

